// Description: Demonstrates the use of If-Else statements in C++.

#include<iostream>
using namespace std;

int main() {
    int fatima_age = 19;
    int khushi_age = 20;

    if (fatima_age > khushi_age) {
        cout << "Fatima is older than Khushi" << endl;
    } else {
        cout << "Khushi is older than Fatima" << endl;
    }

    return 0;
}
