
// Description: Demonstrates different data types in C++.

#include<iostream>  
using namespace std;  

int main() {  
    int num = 10;  
    cout << "Integer: " << num << endl;  

    float num1 = 20.356f;  
    cout << "Float: " << num1 << endl;  

    double num2 = 234.567987654;  
    cout << "Double: " << num2 << endl;  

    string str = "Pawan Maurya";  
    cout << "String: " << str << endl;  

    char ch = 'A';  
    cout << "Character: " << ch << endl;  

    bool value = false;  
    cout << "Boolean: " << value << endl;  // 0 (false)  

    return 0;  
}
